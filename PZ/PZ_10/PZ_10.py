# Имеется список студентов группы, в котором все имена различны. Определить, есть ли в
# группе студент, который побывал в гостях у всех студентов. (Для каждого студента
# составить список из множества побывавших у него в гостях друзей, причем хозяина в этот
# список не включать).

# Список всех студентов
students = ["Андрей", "Виктор", "Максим", "Ольга", "Елена", "Дмитрий"]

# Списки гостей для каждого студента (в том же порядке, что и students)
guest_lists = [
    {"Виктор", "Максим"},       # Гости Андрея
    {"Андрей", "Максим", "Ольга"},  # Гости Виктора
    {"Виктор", "Ольга"},        # Гости Максима
    {"Андрей", "Виктор", "Максим", "Елена"},  # Гости Ольги
    {"Ольга", "Дмитрий"},       # Гости Елены
    {"Елена"}                   # Гости Дмитрия
]

# Найдем студента, который был в гостях у всех остальных
universal_guest = None

for i, guest in enumerate(students):
    # Множество хозяев, у которых был этот гость
    visited_hosts = set()
    
    for j, host_guests in enumerate(guest_lists):
        if guest in host_guests:
            visited_hosts.add(students[j])  # Добавляем хозяина
    
    # Проверяем, посетил ли гость всех, кроме себя
    if visited_hosts == set(students) - {guest}:
        universal_guest = guest
        break

if universal_guest:
    print(f"Студент, который был в гостях у всех: {universal_guest}")
else:
    print("Нет студента, который был в гостях у всех.")
