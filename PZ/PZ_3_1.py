# Дано целое положительное число. Проверить истинность высказывания: «Данное
# число является нечетным трехзначным».

try:
    # Ввод числа
    number = int(input("Введите целое положительное число: "))

    # Проверка, является ли число трёхзначным и нечётным
    if 100 <= number <= 999 and number % 2 != 0:
        print("Высказывание истинно: число является нечётным трёхзначным.")
    else:
        print("Высказывание ложно.")
except ValueError:
    print("Ошибка: введено не целое число!")
