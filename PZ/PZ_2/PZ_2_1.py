def transform_number(number):
    # Проверяем, что число является трехзначным (от 100 до 999 включительно)
    if not (100 <= number < 1000):
        # Если число не трехзначное, поднимаем исключение с сообщением об ошибке
        raise ValueError("Ввод должен быть трёхзначным числом.")
    # Преобразуем число: меняем местами сотни и десятки
    return (number % 100) * 10 + number // 100

def main():
    try:
        # Запрашиваем у пользователя ввод трехзначного числа
        user_input = int(input("Введите трехзначное число: "))
        # Вызываем функцию преобразования и сохраняем результат
        result = transform_number(user_input)
        # Выводим преобразованное число
        print(f"Преобразованное число: {result}")
    except ValueError as e:
        # Обрабатываем ошибки ввода и выводим сообщение об ошибке
        print(f"Ошибка: {e}")

# Запускаем функцию main, если скрипт выполняется как основной модуль
if __name__ == "__main__":
    main()
